    private void setCustomer() {
        fName.setText(cust.getFname());
        lName.setText(cust.getLname());
        address1.setText(cust.getAddress1());
        address2.setText(cust.getAddress2());
        city.setText(cust.getCity());
        state.setText(cust.getState());
        zip.setText(cust.getZip());
        phone.setText(cust.getPhone());
        
        if(cust.getMembership() != null){
            membershipEditButton.setVisible(true);
            membershipCreateButton.setVisible(false);
        }else{
            membershipCreateButton.setVisible(true);
            membershipEditButton.setVisible(false);
        }
    }
    
