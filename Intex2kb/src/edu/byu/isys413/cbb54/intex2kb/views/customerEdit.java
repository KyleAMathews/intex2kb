    private void getByNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getByNameActionPerformed
        List list = null;
        try{
            list = (List)CustomerDAO.getInstance().getByName(getFirst.getText(),getLast.getText());
            
            if(list.size()!=0){
                cust = (Customer)list.get(0);
                setCustomer();
            }
        }catch (DataException e){
            JOptionPane.showMessageDialog(null,"There was an error during the search");
        }
        
        getFirst.setText("");
        getLast.setText("");
    }//GEN-LAST:event_getByNameActionPerformed

